numero1 = int( input('Digite um numero: ') )
numero2 = int( input('Digite outro numero: ') )

soma = numero1 + numero2
subt = numero1 - numero2
mult = numero1 * numero2
divi = numero1 / numero2
rest = numero1 % numero2
pote = numero1 ** numero2

print('Soma:', soma)
print('Subtracao:', subt)
print('Multiplicacao:', mult)
print('Divisao:', divi)
print('Resto da divisao:', rest)
print('Potencia:', pote)