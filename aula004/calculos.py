def soma(n1, n2):
    resultado = n1 + n2
    print(f"Soma: {resultado}")

def subtracao(n1, n2):
    resultado = n1 - n2
    print(f"Subtracao: {resultado}")

def multiplicacao(n1, n2):
    resultado = n1 * n2
    print(f"Multiplicacao: {resultado}")

def divisao(n1, n2):
    resultado = n1 / n2
    print(f"Divisao: {resultado}")
